using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using Microsoft.AspNetCore.Mvc.Rendering;
using Microsoft.EntityFrameworkCore;
using RealEstatePortal.Models;

namespace RealEstatePortal.Pages.Buyer
{
    public class EditModel : PageModel
    {
        private readonly RealEstatePortal.Models.PortalContext _context;

        public EditModel(RealEstatePortal.Models.PortalContext context)
        {
            _context = context;
        }

        [BindProperty]
        public Buyer Buyer { get; set; }

        public async Task<IActionResult> OnGetAsync(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            Buyer = await _context.Buyer
                .Include(b => b.ContactDetails).FirstOrDefaultAsync(m => m.BuyerID == id);

            if (Buyer == null)
            {
                return NotFound();
            }
           ViewData["ContactDetailsID"] = new SelectList(_context.Set<ContactDetails>(), "ContactDetailsID", "ContactDetailsID");
            return Page();
        }

        public async Task<IActionResult> OnPostAsync()
        {
            if (!ModelState.IsValid)
            {
                return Page();
            }

            _context.Attach(Buyer).State = EntityState.Modified;

            try
            {
                await _context.SaveChangesAsync();
            }
            catch (DbUpdateConcurrencyException)
            {
                if (!BuyerExists(Buyer.BuyerID))
                {
                    return NotFound();
                }
                else
                {
                    throw;
                }
            }

            return RedirectToPage("./Index");
        }

        private bool BuyerExists(int id)
        {
            return _context.Buyer.Any(e => e.BuyerID == id);
        }
    }
}
